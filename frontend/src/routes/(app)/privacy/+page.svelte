	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">1. Topladığımız Veriler</h2>
		<ul class="list-disc pl-5 text-sm text-neutral-600 dark:text-neutral-400 space-y-1">
			<li><strong>Zorunlu:</strong> Kullanıcı adı ve şifre (şifreniz geri döndürülemez şekilde hash'lenir).</li>
			<li><strong>İsteğe bağlı:</strong> E-posta (yalnızca siz isterseniz; hesap kurtarma için).</li>
			<li><strong>İçerik:</strong> Yüklediğiniz gönderiler, mesajlar ve profil bilgileri.</li>
			<li><strong>Ödeme:</strong> Kripto işlem kayıtları (cüzdan adresleri, tutarlar). Banka veya kart bilgisi alınmaz.</li>
		</ul>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">2. Toplamadığımız Veriler</h2>
		<ul class="list-disc pl-5 text-sm text-neutral-600 dark:text-neutral-400 space-y-1">
			<li>Gerçek isim, kimlik belgesi veya yüz fotoğrafı.</li>
			<li>Erişim loglarında IP adresi, tarayıcı veya referer bilgisi.</li>
			<li>Kredi kartı / banka hesabı bilgileri.</li>
			<li>Üçüncü taraf reklam veya takip çerezleri.</li>
		</ul>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">3. Anonimlik</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			Monero (XMR) ile yapılan ödemeler tasarımı gereği özeldir. Bitcoin işlemleri
			blok zincirinde herkese açıktır; daha yüksek gizlilik için Monero önerilir.
			Profiller arama motorlarında indekslenmeyecek şekilde işaretlenir.
		</p>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">4. Verilerin Kullanımı</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			Verileriniz yalnızca hizmeti sunmak (abonelik, ödeme, mesajlaşma) için kullanılır.
			Verilerinizi satmıyor veya reklam amacıyla üçüncü taraflarla paylaşmıyoruz.
		</p>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">5. Hesabınızı Silme</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			İstediğiniz zaman hesabınızı silebilirsiniz. Yasal yükümlülükler dışında
			verileriniz kaldırılır.
		</p>
	</section>

