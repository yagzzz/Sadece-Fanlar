	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">1. Yaş Sınırı (18+)</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			SadeceFanlar yalnızca 18 yaşından büyük yetişkinler içindir. Platforma kayıt
			olarak ve içeriklere erişerek 18 yaşından büyük olduğunuzu beyan etmiş olursunuz.
			Reşit olmayan kişiler bu platformu kullanamaz.
		</p>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">2. Yasak İçerikler</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			Aşağıdaki içerikler kesinlikle yasaktır ve tespit edildiğinde anında kaldırılır,
			hesap kalıcı olarak kapatılır:
		</p>
		<ul class="list-disc pl-5 text-sm text-neutral-600 dark:text-neutral-400 space-y-1">
			<li>Reşit olmayan kişileri içeren her türlü içerik (sıfır tolerans).</li>
			<li>Rıza dışı paylaşılan içerik (intikam pornosu vb.).</li>
			<li>Şiddet, insan ticareti, uyuşturucu ticareti veya diğer yasa dışı faaliyetler.</li>
			<li>Başkasının kimliğine bürünme veya telif hakkı ihlali.</li>
			<li>Dolandırıcılık, spam veya zararlı yazılım.</li>
		</ul>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">3. İçerik Üreticisinin Sorumlulukları</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			İçerik üreticileri, paylaştıkları tüm içeriklerin kendilerine ait olduğunu veya
			paylaşma hakkına sahip olduklarını, içerikteki tüm kişilerin 18 yaşından büyük
			olduğunu ve içeriğin paylaşımına rıza gösterdiğini kabul eder.
		</p>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">4. Ödemeler</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			Tüm ödemeler yalnızca kripto para (Monero ve Bitcoin) ile yapılır. Kripto para
			fiyat dalgalanmaları nedeniyle tutarlar değişebilir. Yanlış cüzdan adresine
			yapılan transferler geri alınamaz. Platform komisyon oranları cüzdan ve ödeme
			ekranlarında açıkça belirtilir.
		</p>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">5. Hesap Güvenliği</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			Hesabınızın güvenliğinden siz sorumlusunuz. İki adımlı doğrulamayı (2FA)
			etkinleştirmenizi öneririz. E-posta vermeden kaydolduysanız, şifrenizi
			kaybetmeniz halinde hesabınızı kurtarmak mümkün olmayabilir.
		</p>
	</section>

	<section class="space-y-2">
		<h2 class="text-lg font-semibold text-neutral-900 dark:text-white">6. Raporlama ve Moderasyon</h2>
		<p class="text-sm text-neutral-600 dark:text-neutral-400">
			Kurallara aykırı içerikleri bildirebilirsiniz. Bildirimler incelenir ve gerekli
			işlemler uygulanır. Platform, kuralları ihlal eden hesapları askıya alma veya
			kapatma hakkını saklı tutar.
		</p>
	</section>

